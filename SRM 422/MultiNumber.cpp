class MultiNumber {
    public:
    int give_length(int n){
        int cnt=0;
        while(n>0){
            n/=10;
            ++cnt;
        }
        return cnt;
    }
    string check(int number) {
        int n=give_length(number);
        if(n==1){
            return "NO";
        }
        int a[n];
        for(int i=0;i<n;++i){
            a[n-1-i]=number%10;
            number/=10;
        }
        int n1=1;
        for(int i=0;i<n;++i){
            n1=n1*a[i];
            int n2=1;
            for(int j=i+1;j<n;++j){
                n2=n2*a[j];
            }
            if(n1 == n2){
                return "YES";
            }
        }
        return "NO";
    }
};

