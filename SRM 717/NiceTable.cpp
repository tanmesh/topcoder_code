class NiceTable {
    public:
    string isNice(vector<string> t) {
        int n = t.size(), m = t[0].size();
        
        int x[100], y[100];
         
        x[0] = 0, y[0] = t[0][0] - '0';
        for (int i = 1; i < n; ++i) x[i] = (t[i][0] - '0') ^ y[0];
        for (int i = 1; i < m; ++i) y[i] = (t[0][i] - '0') ^ x[0];
         
        for (int i = 0; i < n; ++i)
            for (int j = 0; j < m; ++j)
                if (t[i][j] - '0' != (x[i] ^ y[j]))
                    return "Not nice";
         
        return "Nice";
    }
};

