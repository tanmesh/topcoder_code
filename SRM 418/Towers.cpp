class Towers {
    public:
    int attack(int myUnits, int hpT, int attackT, int numT) {
        int all_pt=numT*hpT,ans=0;
        while(all_pt>0 && myUnits > 0){
            ++ans;
            all_pt-=myUnits;
            if(all_pt < 0) break;
            myUnits-=attackT*((all_pt+hpT-1)/hpT);
        }
        if(myUnits>0) return ans;
        return -1;
    }
};

