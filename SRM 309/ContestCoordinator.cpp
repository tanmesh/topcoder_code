class ContestCoordinator {
    public:
    double bestAverage(vector<int> scores) {
        double m=scores.size(),max=-1;
        sort(scores.begin(),scores.end());
        int n=m;
        for(int i=0;i<(m/2);++i){
            double sum=0;
            for(int j=0;j<n;++j){
                sum+=scores[j];
            }
            double avg=(double)sum/n;
            if(max<avg){
                max=avg;
            }
            scores.erase(scores.begin());
            scores.pop_back();
            n=n-2;
        }
        return max;
    }
};

