class AmoebaDivTwo {
    public:
    int count(vector<string> table, int k) {
        int cnt=0;
        string s="";
        for(int i=0;i<k;++i) s+='A';
        for(int i=0;i<table.size();++i){
            for(int j=0;j<table[i].size();++j){
                if(table[i].substr(j,k) == s) ++cnt;
            }
        }
        if(k!=1){
            for(int j=0;j<table[0].size();++j){
                for(int i=0;i<table.size();++i){
                    string str="";
                    int tmp=0,l=i;
                    while(tmp<k && l<table.size()){
                        str+=table[l][j];
                        ++tmp,++l;
                    }   

                    if(str == s) ++cnt;
                }
            }
        }
        return cnt;
    }
};

