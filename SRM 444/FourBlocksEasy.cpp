class FourBlocksEasy {
    public:
    int maxScore(vector<string> grid) {
        int total_dot=0;
        for(int i=0;i<grid.size();++i){
            for(int j=0;j<grid[i].length();++j){
                if(grid[i][j] == '.') ++total_dot;
            }
        }
        int cnt=0;
        for(int i=0;i+1<grid.size();++i){
            for(int j=0;j+1<grid[i].length();++j){
                if(grid[i][j] == '.'){
                    if(grid[i][j+1] == '.'){
                        if(grid[i+1][j] == '.'){
                            if(grid[i+1][j+1] == '.'){
                                cnt++;
                                ++j;
                            }
                        }
                    }
                }
            }
        }
        int four_scr=cnt*16 , one_scr=grid.size()*grid[0].length()-cnt*4;
        return four_scr+one_scr;
    }
};

