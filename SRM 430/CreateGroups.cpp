class CreateGroups {
    public:
    int minChanges(vector<int> groups, int minSize, int maxSize) {
        int max_numb=0,min_numb=0,N=groups.size(),total_std=0;
        for(int i=0;i<N;++i){
            total_std+=groups[i];
        }
        if(total_std > N*maxSize || total_std < N*minSize) return -1;
        for(int i=0;i<N;++i){
            if(groups[i] < minSize) min_numb+=minSize-groups[i];
            if(groups[i] > maxSize) max_numb+=groups[i]-maxSize;
        }
        return max(max_numb,min_numb);
    }
};

